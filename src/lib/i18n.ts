export function t(locale: Locale, key: string): string {
  return DICTIONARY[locale][key] ?? DICTIONARY.en[key] ?? key;
}
export function columnLabel(locale: Locale, column: ReportColumn): string {
  return COLUMN_LABELS[column][locale === 'fr' ? 1 : 0];
}
export function statusLabel(locale: Locale, status: string): string {
  return t(locale, status === 'refunded' ? 'refundedStatus' : status);
}
export function reportTitle(locale: Locale, kind: ReportKind): string {
  return t(locale, kind);
}
export function reportDescription(locale: Locale, kind: ReportKind): string {
  return t(locale, `${kind}Description`);
}
